import java.util.*;
public class elseif {
    public static void main (String[] args)
 { 
    int age=14;

    if (age>=18)
    { 
        System.out.print("adult");
    }
    else if(age>=13 && age<=18)
    {
        System.out.print("tenager");
    }else
    {
        System.out.print("child");
    } 
 }
    



}


    

